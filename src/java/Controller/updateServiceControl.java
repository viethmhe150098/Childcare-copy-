    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //        processRequest(request, response);
        DAOService dao = new DAOService();
        String sID = request.getParameter("sID");
        Service ser = dao.getServiceByCID1(sID);
        request.setAttribute("service", ser);
        request.getRequestDispatcher("UpdateService.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        DAOService dao = new DAOService();
        int sid = Integer.parseInt(request.getParameter("sid"));
        String sname = request.getParameter("sname");
        String image = request.getParameter("image");
        int maxquantity = Integer.parseInt(request.getParameter("maxquantity"));
        double sprice = Double.parseDouble(request.getParameter("sprice"));
        String description = request.getParameter("description");
        dao.updateService(sname, sprice, description, maxquantity, image, sid);
        response.sendRedirect("ServiceControl");
    }

